<h5 >Propriété intellectuelle</h5>

<strong >Textes</strong>
<p >La reproduction, intégrale ou partielle, des textes publiés sur le site babule.fr n'est pas soumise à une autorisation préalable. Cette utilisation doit néanmoins mentionner explicitement et obligatoirement la référence à la Bibliothèque municipale de Babule ainsi que l'url du site source de l'article. 
Les utilisateurs des contenus éditoriaux du site Babule.fr informeront la Bibliothèque de l'usage qui en a été fait.</p>

<strong >Illustrations</strong>
<p >Les photos, images fixes et animées, ne peuvent être reproduites sans autorisation préalable sollicitée par mail auprès de la Bibliothèque. La demande précisera le support de reproduction et son mode de diffusion. Les photos et les images utilisées après autorisation mentionneront le copyright de la Bibliothèque municipale de Babule ou celui indiqué au moment de l'autorisation.
La reproduction de contenus éditoriaux, de photos et d'images dans un but commercial ou publicitaire n'est pas autorisée.</p>

<strong ">Liens hypertexte</strong>
<p >Les liens pointant vers la page d'accueil ou l'une des pages du site Babule.fr peuvent être établis sans autorisation préalable. Dans ce cas la mention explicite du lien "Site de la Bibliothèque municipale de Babule" sera faite pour informer l'internaute de la teneur du lien. Cette autorisation tacite n'est pas valable pour les sites ayant un but commercial ou publicitaire ni aux sites internet diffusant des informations à caractère illicite, violent, polémique, pornographique, xénophobe ou pouvant porter atteinte à la sensibilité du plus grand nombre.</p>

<h5>Traitement des données personnelles</h5>

<p>Pour utiliser les services accessibles depuis le site, les internautes utilisent soit leur numéro d’adhérent et mot de passe, soit leur compte MonBabule.
Lors de la création du compte MonBabule.fr, un courriel valide est demandé.
Ce courriel est utilisé par la Bibliothèque uniquement pour la bonne gestion des services en ligne.</p>

<strong>Formulaires de contact</strong>
<p>Les contacts du site Babule.fr sont gérés par formulaires électroniques. La plupart des informations demandées sont obligatoires (nom, prénom, courriel...). Ces informations sont collectées dans le seul but de répondre à la demande.
Il n'est procédé à aucun archivage de ces données.</p>

<strong>Lettre d'information</strong>
<p>Une lettre d'information mensuelle (ou newsletter) est proposée à tous les abonnés lors de leur inscription ou aux visiteurs du site www.bm-Babule.fr. 
Lors de l'inscription, un courriel valide est demandé. Votre adresse de messagerie ne sera utilisée qu’en vue de vous transmettre la lettre d’information. Elle ne sera pas utilisée à d’autres fins ni cédée. 
Un lien dans la lettre d'information permet de se désabonner en un clic. Votre adresse ne sera pas conservée si vous vous désabonnez.</p>

<strong>Droit d’accès, de modification et de suppression des informations personnelles</strong>
<p>Toute personne abonnée à la lettre d'information ou utilisant les services en ligne du site peut avoir accès aux informations personnelles la concernant et en demander la modification ou la suppression en s’adressant à info@bm-Babule.fr</p>

